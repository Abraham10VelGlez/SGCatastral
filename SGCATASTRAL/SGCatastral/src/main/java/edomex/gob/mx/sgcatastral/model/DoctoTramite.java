package edomex.gob.mx.sgcatastral.model;

public class DoctoTramite {
    
    private Number id;
    private Number id_doc;
    private String cve_cat;
    private String ruta;
    private String documento;

    public Number getId_doc() {
        return id_doc;
    }

    public void setId_doc(Number id_doc) {
        this.id_doc = id_doc;
    }

    public String getCve_cat() {
        return cve_cat;
    }

    public void setCve_cat(String cve_cat) {
        this.cve_cat = cve_cat;
    }

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public Number getId() {
        return id;
    }

    public void setId(Number id) {
        this.id = id;
    }

    public String getDocumento() {
        return documento;
    }

    public void setDocumento(String documento) {
        this.documento = documento;
    }

    
      
}
